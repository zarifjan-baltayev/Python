# if temperature is greater than 30'C'
#     it is a hot day
# otherwise if it is less than 10'C
#     it is acold day
# otherwise
#     it is neither hot nor cold



temperature = 9

if temperature > 30:
    print("It is a hot day!")
elif temperature < 10:
    print("It is a cold day")
else:
    print("It is neither hot nor cold!")